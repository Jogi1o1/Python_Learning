# F-String is used to typeconvert any other datatype into string.
# Here we are defining three types of variables with different datatypes and than we will print them using F-String.
# Here we are defining a variable Height
Height = 1.5
# Here we are defining a variable Nick_Name
Nick_Name = "Balya"
# Here we are defining the age of the user.
Age = 25
# Here we are printing the variable Height with a string using F-String
print(f"Your Height is {Height} cm")
# Here we are printing the variable Nick_Name with a string using F-String
print(f"Your Nick Name is {Nick_Name}")
# Here we are printing the age of user
print(f"Your age is {Age} years")
