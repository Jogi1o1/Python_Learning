# sort() function is used to sort the list in ascending order by default
# Creating a list here
num_list = [1,7,53,64,3453,1,56,7,5,53,324]
# sorting this list in ascending order
num_list.sort()
# printing this list in ascending order
print(num_list)
# to sort list in descending order we use sort(reverse = True)
num_list.sort(reverse=True)
# printing descending order
print(num_list)