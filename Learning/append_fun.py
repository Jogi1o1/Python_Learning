# append(item_value) function is used to add new value to a list for e.g.
# Lets create a list first
List_of_groceries = ["Spinach", "Panner", "Garam Masala"]
# Now lets use append function to add a new item at the end of the list
List_of_groceries.append("Chat Masala")
# Now lets print the list
print(List_of_groceries)
# below written code also works as append function
List_of_groceries.insert(len(List_of_groceries),"Chole Masala")
print(List_of_groceries)
